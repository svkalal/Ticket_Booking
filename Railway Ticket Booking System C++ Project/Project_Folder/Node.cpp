#include "Node.h"
////////////////////////////////////////////////
Node::Node(Pass d)
{
	this->data=d;
	this->next=NULL;
}
////////////////////////////////////////////////
Pass Node::getdata()
{
	return data;
}
////////////////////////////////////////////////
Node* Node::getnext()
{
	return this->next;
}
////////////////////////////////////////////////
void Node::setdata(Pass dt)
{
	this->data=dt;
}
////////////////////////////////////////////////
void Node::setnext(Node* pt)
{
	this->next=pt;
}
